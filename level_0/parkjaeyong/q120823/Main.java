package level_0.parkjaeyong.q120823;


import java.util.Scanner;

class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        for(int i = 1 ; i <= n ; i++){
            System.out.println("*".repeat(i));
        }
    }
}




